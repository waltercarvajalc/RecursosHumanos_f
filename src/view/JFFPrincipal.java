    private boolean guardar(){
        Funcionario funcionario = new Funcionario();
        TipoIdentificacion tipoId = new TipoIdentificacion(jCBTipoDocumento.getSelectedIndex() + 1);
        funcionario.setTipoIdentificacion(tipoId);
        funcionario.setNumeroIdentificacion(jTFDocumento.getText().trim());
        funcionario.setNombres(jTFNombres.getText().trim());
        funcionario.setApellidos(jTFApellidos.getText().trim());
        funcionario.setSexo(jCBSexo.getSelectedItem().toString().charAt(0));
        funcionario.setTelefono(jFFTelefono.getText());
        if(JDChFechaNac.getDate() != null){
            funcionario.setFechaNacimiento(JDChFechaNac.getDate().toInstant().atZone(ZoneId.systemDefault()).toLocalDate());
        }else{
            funcionario.setFechaNacimiento(LocalDate.now(ZoneId.systemDefault()));
        }
        EstadoCivil estadoCivil = new EstadoCivil(JCBEstadoCivil.getSelectedIndex() + 1);
        funcionario.setEstadoCivil(estadoCivil);
        funcionario.setDireccion(jTADireccion.getText().trim());
        int resultado = funcionarioCtrl.guardar(funcionario);
        if (resultado > 0){
            return true;
        }
        return false;
    }
